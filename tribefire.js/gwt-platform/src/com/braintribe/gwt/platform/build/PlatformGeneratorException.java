package com.braintribe.gwt.platform.build;

@SuppressWarnings("serial")
public class PlatformGeneratorException extends Exception {

	public PlatformGeneratorException() {
		super();
	}

	public PlatformGeneratorException(String message, Throwable cause) {
		super(message, cause);
	}

	public PlatformGeneratorException(String message) {
		super(message);
	}

	public PlatformGeneratorException(Throwable cause) {
		super(cause);
	}
	
}
