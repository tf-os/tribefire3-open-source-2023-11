package com.braintribe.build.ravenhurst.scanner;

@SuppressWarnings("serial")
public class ScannerException extends Exception {

	public ScannerException() {
		super();

	}

	public ScannerException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);

	}

	public ScannerException(String arg0, Throwable arg1) {
		super(arg0, arg1);

	}

	public ScannerException(String arg0) {
		super(arg0);

	}

	public ScannerException(Throwable arg0) {
		super(arg0);

	}

}
