@SuppressWarnings("serial")
public class SourceRepositoryAccessException extends RuntimeException {

	public SourceRepositoryAccessException() {
	}

	public SourceRepositoryAccessException(String arg0) {
		super(arg0);
	}

	public SourceRepositoryAccessException(Throwable arg0) {
		super(arg0);
	}

	public SourceRepositoryAccessException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
