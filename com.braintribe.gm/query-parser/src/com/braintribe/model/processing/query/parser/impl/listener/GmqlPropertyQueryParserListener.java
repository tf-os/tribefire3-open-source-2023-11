package com.braintribe.model.processing.query.parser.impl.listener;

import com.braintribe.model.generic.value.EntityReference;
import com.braintribe.model.generic.value.PersistentEntityReference;
import com.braintribe.model.processing.query.parser.api.GmqlQueryParserException;
import com.braintribe.model.processing.query.parser.impl.DefaultSource;
import com.braintribe.model.processing.query.parser.impl.autogenerated.GmqlParser.PropertyQueryContext;
import com.braintribe.model.processing.query.parser.impl.autogenerated.GmqlParser.PropertyQueryHeadContext;
import com.braintribe.model.processing.query.parser.impl.autogenerated.GmqlParser.PropertyQueryStartContext;
import com.braintribe.model.processing.query.parser.impl.context.EntityReferenceCustomContext;
import com.braintribe.model.processing.query.parser.impl.context.QueryCustomContext;
import com.braintribe.model.processing.query.parser.impl.context.basetype.DefaultCustomContext;
import com.braintribe.model.query.PropertyQuery;
import com.braintribe.model.query.Source;

public abstract class GmqlPropertyQueryParserListener extends GmqlEntityQueryParserListener {

	@Override
	public void enterPropertyQuery(PropertyQueryContext ctx) {
		PropertyQuery query = $.propertyQuery();
		setValue(ctx, new QueryCustomContext(query));
		enableQueryHeadParsing();
		enableDefaultSource();
	}

	@Override
	public void exitPropertyQuery(PropertyQueryContext ctx) {
		takeValue(ctx.propertyQueryStart());
		takeValue(ctx.queryTailWhere());
		takeValue(ctx.queryTailOrderBy());
		takeValue(ctx.queryTailPagination());
		disableDefaultSource();
	}

	@Override
	public void exitPropertyQueryStart(PropertyQueryStartContext ctx) {
		PropertyQuery query = (PropertyQuery) ((QueryCustomContext) getValue(ctx.getParent()).cast()).getReturnValue();
		
		if (ctx.Distinct() != null) {
			query.setDistinct(true);
		}
		String propertyName = ((DefaultCustomContext) takeValue(ctx.propertyQueryHead()).cast()).getReturnValue();
		query.setPropertyName(propertyName);

		EntityReference entityReference = ((EntityReferenceCustomContext) takeValue(ctx.entityReference()).cast()).getReturnValue();
		if(!(entityReference instanceof PersistentEntityReference)){
			setCustomParsingExcpetion(new GmqlQueryParserException("Only PersistentEntityReference is allowed for PropertyQuery, provided value: " + entityReference));
			throw new RuntimeException();
		}
		query.setEntityReference((PersistentEntityReference) entityReference);

		// to avoid default rule handling to fire
		setValue(ctx, new DefaultCustomContext(""));
	}
	
	
	@Override
	public void exitPropertyQueryHead(PropertyQueryHeadContext ctx) {
		String entityTypeSignature = ((DefaultCustomContext) takeValue(ctx.identifier()).cast()).getReturnValue();

		Source source = DefaultSource.T.create();
		String alias = null;
		if (ctx.alias() != null) {
			alias = ((DefaultCustomContext) takeValue(ctx.alias()).cast()).getReturnValue();
		}
		registerSource(alias, source);
		disableQueryHeadParsing();
		setValue(ctx, new DefaultCustomContext(entityTypeSignature));
	}
}
