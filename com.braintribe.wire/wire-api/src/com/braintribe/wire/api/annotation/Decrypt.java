package com.braintribe.wire.api.annotation;

import java.lang.annotation.ElementType;
import java.lang.annotation.Inherited;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

@Retention(RetentionPolicy.RUNTIME)
@Target(ElementType.METHOD)
@Inherited
public @interface Decrypt {
	String secret() default "c36e99ec-e108-11e8-9f32-f2801f1b9fd1";
	SecretResolution secretResolution() default SecretResolution.VALUE;
	String algorithm() default "AES/CBC/PKCS5Padding";
	String keyFactoryAlgorithm() default "PBKDF2WithHmacSHA1";
	int keyLength() default 128;
}
